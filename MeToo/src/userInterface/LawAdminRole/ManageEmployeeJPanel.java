    public void populateOrganizationComboBox(){
        organizationJComboBox.removeAllItems();
        
        for (Organization organization : organizationDir.getOrganizationList()){
            organizationJComboBox.addItem(organization);
        }
    }
    
    public void populateOrganizationEmpComboBox(){
        organizationEmpJComboBox.removeAllItems();
        
        for (Organization organization : organizationDir.getOrganizationList()){
            organizationEmpJComboBox.addItem(organization);
        }
    }
    
    public void populateSpecialtyComboBox() {
        specialtyJComboBox.removeAllItems();
        specialtyJComboBox.addItem("Criminal");
        specialtyJComboBox.addItem("Marital");
        specialtyJComboBox.addItem("Public Interest");
        specialtyJComboBox.addItem("Mental Health");
        specialtyJComboBox.addItem("Sexual Assault"); 
        specialtyJComboBox.addItem("Sexual Harassment");        
        
                
    }
    
    private void populateAssistantComboBox() {
        assistantJComboBox.removeAllItems();
        for (Organization organization : organizationDir.getOrganizationList()) {
            if(organization instanceof AssistantOrganization) {
                for(UserAccount u : organization.getUserAccountDirectory().getUserAccountList()) {
                    assistantJComboBox.addItem(u);
                }
            }
        }
    }

    private void populateTable(Organization organization){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[2];
            row[0] = employee.getId();
            row[1] = employee.getName();
            model.addRow(row);
        }
    }

